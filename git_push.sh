#!/bin/bash

# Скрипт для автоматического добавления, коммита и отправки изменений на GitHub.

# 1. Проверяем, было ли предоставлено сообщение для коммита.
if [ -z "$1" ]; then
  echo "Ошибка: Пожалуйста, укажите сообщение для коммита в кавычках."
  echo "Пример использования: bash git_push.sh "Исправлена ошибка в конвертере""
  exit 1
fi

# 2. Добавляем все измененные и новые файлы в индекс.
echo "1. Добавление файлов в индекс (git add .)..."
git add .

# 3. Создаем коммит с сообщением, переданным в качестве аргумента.
echo "2. Создание коммита (git commit)..."
git commit -m "$1"

# 4. Отправляем изменения в удаленный репозиторий на GitHub.
echo "3. Отправка на GitHub (git push)..."
git push

echo "Готово! Все изменения успешно отправлены на GitHub."
